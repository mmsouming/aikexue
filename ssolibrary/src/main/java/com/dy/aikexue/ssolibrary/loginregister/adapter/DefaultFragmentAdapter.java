package com.dy.aikexue.ssolibrary.loginregister.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import java.util.List;

/**
 * Created by zhong on 2017/2/22.
 */

public class DefaultFragmentAdapter extends FragmentStatePagerAdapter {
    private List<Fragment> list;

    public DefaultFragmentAdapter(FragmentManager fm, List<Fragment> list) {
        super(fm);
        this.list = list;
    }

    @Override
    public Fragment getItem(int position) {
        return list.get(position);
    }

    @Override
    public int getCount() {
        return list.size();
    }
}
